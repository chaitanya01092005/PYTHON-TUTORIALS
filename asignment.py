# Initialize variable
x = 10

# Assign and print
x = 10  # Assignment operator
print(f"x = {x}")  # Output: 10

# Add and assign
x += 5  # Equivalent to: x = x + 5
print(f"x += 5 -> {x}")  # Output: 15

# Subtract and assign
x -= 3  # Equivalent to: x = x - 3
print(f"x -= 3 -> {x}")  # Output: 12

# Multiply and assign
x *= 2  # Equivalent to: x = x * 2
print(f"x *= 2 -> {x}")  # Output: 24

# Divide and assign
x /= 4  # Equivalent to: x = x / 4
print(f"x /= 4 -> {x}")  # Output: 6.0

# Floor division and assign
x //= 2  # Equivalent to: x = x // 2
print(f"x //= 2 -> {x}")  # Output: 3.0

# Modulus and assign
x %= 2  # Equivalent to: x = x % 2
print(f"x %= 2 -> {x}")  # Output: 1.0

# Exponentiation and assign
x **= 3  # Equivalent to: x = x ** 3
print(f"x **= 3 -> {x}")  # Output: 1.0

# Bitwise AND and assign
x = 5  # Reset x to 5 (binary: 101)
x &= 3  # Equivalent to: x = x & 3 (binary: 011)
print(f"x &= 3 -> {x}")  # Output: 1

# Bitwise OR and assign
x |= 2  # Equivalent to: x = x | 2 (binary: 010)
print(f"x |= 2 -> {x}")  # Output: 3

# Bitwise XOR and assign
x ^= 1  # Equivalent to: x = x ^ 1 (binary: 001)
print(f"x ^= 1 -> {x}")  # Output: 2

# Bitwise left shift and assign
x <<= 2  # Equivalent to: x = x << 2
print(f"x <<= 2 -> {x}")  # Output: 8

# Bitwise right shift and assign
x >>= 1  # Equivalent to: x = x >> 1
print(f"x >>= 1 -> {x}")  # Output: 4
